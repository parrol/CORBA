    private void b_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_aceptarActionPerformed
        selectedIndex = clientes.getSelectedIndex();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(null, "Ingrese un cliente");
        } else {
            Persona p = list_model.getPersona(selectedIndex);
            System.out.println("Persona seleccionada al final: " + p.getNombre());
            cliente = p.getNombre();
            this.setVisible(false);
            Interfaz.getInterfaz(2).setVisible(true);
        }


    }//GEN-LAST:event_b_aceptarActionPerformed

