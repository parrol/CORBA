    private void b_anadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_anadirActionPerformed
        Persona p = new Persona(fld_nombre.getText(), fld_cedula.getText(), fld_direccion.getText());
        list_model.add(p);
    }//GEN-LAST:event_b_anadirActionPerformed

    private void b_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_eliminarActionPerformed
        selectedIndex = clientes.getSelectedIndex();
        if (selectedIndex != -1) {
            list_model.delete(selectedIndex);
        }
    }//GEN-LAST:event_b_eliminarActionPerformed

    private void limpiarClientes() {
        lbl_info_nombre.setText("");
        lbl_info_cedula.setText("");
        lbl_info_direccion.setText("");
        fld_nombre.setText("");
        fld_cedula.setText("");
        fld_direccion.setText("");
    }

    private void mostrarPersona() {
        selectedIndex = clientes.getSelectedIndex();
        if (selectedIndex != -1) {
            Persona p = list_model.getPersona(selectedIndex);
            lbl_info_nombre.setText(p.getNombre());
            lbl_info_cedula.setText(p.getCedula());
            lbl_info_direccion.setText(p.getDireccion());

        }
    }

